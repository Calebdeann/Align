async function run() {
  console.log(`Updating ${changes.length} exercises...`);

  const headers = {
    'apikey': SERVICE_KEY,
    'Authorization': `Bearer ${SERVICE_KEY}`,
    'Content-Type': 'application/json',
    'Prefer': 'return=minimal',
  };

  let success = 0;
  let failed = 0;
  const failures = [];

  for (const change of changes) {
    const name = change.display_name.trim();
    try {
      const resp = await fetch(`${SUPABASE_URL}/rest/v1/exercises?id=eq.${change.id}`, {
        method: 'PATCH',
        headers,
        body: JSON.stringify({ display_name: name }),
      });
      if (resp.ok) {
        success++;
      } else {
        failed++;
        const text = await resp.text();
        failures.push({ id: change.id, name, status: resp.status, error: text });
      }
    } catch (err) {
      failed++;
      failures.push({ id: change.id, name, error: err.message });
    }
  }

  console.log(`\nDone! ${success} updated, ${failed} failed.`);
  if (failures.length > 0) {
    console.log('\nFailures:');
    failures.forEach(f => console.log(`  ${f.id} (${f.name}): ${f.status || ''} ${f.error}`));
  }
}

run();
